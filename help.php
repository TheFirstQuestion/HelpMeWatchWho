<h2>Terminology</h2>

<p>We use a lot of lingo here at <em>HelpMeWatchWho</em>, and it can get a little confusing. Here's a cheat sheet:</p>

<ul>
	<li><strong>New Who</strong>&#8212;the "reboot" of<em>Doctor Who</em>, from 2005 until today</li>
	<li><strong>Classic Who</strong>&#8212;the original era of the show, from 1963 to 1989</li>
	<li><strong>TVM</strong>&#8212;the television movie made in 1996 starring Paul McGann as the Doctor</li>
	<li><strong>Minisode</strong>&#8212;a short canonical episode</li>
	<li><strong>Alien File</strong>&#8212;an in-character description of an alien</li>
	<li><strong>Other</strong>&#8212;this episode category includes video games and short stories; canonical stories but in a different media format</li>
	<li><strong>Missing Episode</strong>&#8212;an episode missing from records due to BBC tape-wiping policies; can still be wtched via reconstructions</li>
