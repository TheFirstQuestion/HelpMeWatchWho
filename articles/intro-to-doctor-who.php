<h2>Starter Episodes</h2>

<p>Even if you think you're ready to dive in, you might want to try a few episodes before committing to the show. The first episode isn't really the best example of how the great the show can be. We're got a few recomendations for beginning to watch.</p>

<div class="fullSize"><a href="/episodes.php?episodeID=290&seasonID=29"><img src="images/blink.jpg" /></a></div>

<a href="/episodes.php?episodeID=290&seasonID=29"><h2>Blink</h2></a>

<p><b>Why You Should Watch It:</b> <i>Blink</i> is regarded by fans as one of the best episodes of <i>Doctor Who</i>. It's representative of the show in its focus on characters, time-travel craziness, and general creepiness. It's slightly unusual, however, in that the Doctor is barely in the episode. Overall, it's a very good episode, and if you like it, you'll probably like <i>Doctor Who</i>.</p>

<p><b>What It's About:</b> When Sally Sparrow breaks into an old house, she's looking for the perfect picture. Instead, she finds some creepy statues and a mysterious message addressed to her from a man called the Doctor. It's from the year 1969, but he somehow seems able to predict the future. She finds a secret video, hidden on her DVDs, of the Doctor having half a conversation. He's in trouble, and the only person who can save him is her. The only the problem: the creepy statues aren't just statues.</p>



<div class="fullSize"><a href="/episodes.php?episodeID=208&seasonID=22"><img src="images/unicorn and the wasp.jpg" /></a></div>

<a href="/episodes.php?episodeID=208&seasonID=22"><h2>The Unicorn and the Wasp</h2></a>

<p><b>Why You Should Watch It:</b> <i>The Unicorn and the Wasp</i> is one of the more lighthearted episodes of <i>Doctor Who</i>. It shows off the humor of the show, as well as the extent to which the writers love to interact with history. It's a typical historic episode, and gives you an idea of what to expect from the average episode.</p>

<p><b>What It's About:</b> The Doctor and Donna arrive in the 1920's and happen upon Agatha Christie, the famous mystery writer. Soon after, murders start occuring. The whole thing is like one of her books&#8212;a man found mysteriously dead, a thief on the loose, and a long lost family member returning home&#8212;except for the giant wasp on the loose.</p>




<div class="fullSize"><a href="/episodes.php?episodeID=162&seasonID=16"><img src="images/vincent and the doctor.jpg" /></a></div>

<a href="/episodes.php?episodeID=162&seasonID=16"><h2>Vincent and the Doctor</h2></a>

<p><b>Why You Should Watch It:</b> <i>Vincent and the Doctor</i> is another historical episode. It reveals some of the deeper messages in the show, and also gives you a sense of the Eleventh Doctor. It's got a very different feel to it than <i>The Unicorn and the Wasp</i> in that it's much more quiet and introspective; you get a deep look at characters in a way that doesn't really happen in an episode as lighhearted as <i>The Unicorn and the Wasp</i>.</p>

<p><b>What It's About:</b> The Doctor and Amy are visiting an art museum when they find a monster hidden in one of Vincent Van Gough's paintings&#8212;they need to speak to him immediately! They take the TARDIS and happen upon him, becoming his friends and accompanying him on his painting trip. They fight the monster, but end up saving Vincent in a way they had never intended.</p>



<div class="fullSize"><a href="/episodes.php?episodeID=25&seasonID=2"><img src="images/flatline.jpg" /></a></div>

<a href="/episodes.php?episodeID=25&seasonID=2"><h2>Flatline</h2></a>

<p><b>Why You Should Watch It:</b> <i>Flatline</i> will give you a peek into the darker side of <i>Doctor Who</i>. It's got funny moments, but the plot forces the Doctor and Clara to confront issues deeper and darker than terryifying monsters. You get a sense of Twelve; the way his past affects him and the way he tries to avoid it, and you get to see how a companion acts without the Doctor.</p>

<p><b>What It's About:</b> The Doctor is trying to get Clara home, but they're forced to land when something goes wrong with the TARDIS. It shrunk! Something is stealing energy from their ship, and it's up to Clara to find out how to fix it when the Doctor is trapped in the tiny TARDIS.</p>




