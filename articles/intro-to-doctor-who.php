<h2>The Complicated History of <i>Doctor Who</i></h2>

<p><i>Doctor Who</i> is the longest running sci-fi show in the world. It first aired on November 23, 1963. William Hartnell played the Doctor, and he was accompanied by his granddaughter, Susan (Carol Ann Ford), and two of her schoolteachers, Ian Chesterton (William Russel) and Barbara Wright (Jacqueline Hill).</p>

<div class="imgWrapper"><img src="images/quartet.jpg" /></div>

<p>The show was initially very popular, despite the challenges it faced behind the scenes, but its populatiry was cemented after the second serial by the introduction of the Doctor's most famous enemy&#8212;the Daleks.</p>

<div class="imgWrapper"><img src="images/daleks.jpg" /><img src="images/believed.jpg" /></div>

<p>The show continued, old companions leaving and new ones arriving. However, in 1965, Hartnell's arteriosclerosis began to worsen, impacting his ability to remember lines (the show was filmed essentially live). The original production team also departed at this time, and Hartnell didn't get along with the new team. Everyone wanted the show to continue, but it was agreed that Hartnell needed to replaced, just as the many companions had been replaced. But how?</p>

<div class="imgWrapper"><img src="images/hartnell.jpg" /></div>

<p>The writers came up with the idea of "regeneration." When aliens like the Doctor are approaching death, they are able to remain alive by renweing themselves. In the process, they change completely, becoming basically a whole new person&#8212;played by another actor. This is how the show was able to continue without William Hartnell. And this is how the show has continued on for over 50 years.</p>

<div class="imgWrapper"><img src="images/tenth planet 2.jpg" /><img src="images/tenth planet 3.jpg" /><img src="images/tenth planet 4.jpg" /></div>

<p>Over the next 50 years, many people would play the Doctor. Officially, there are twelve Doctors. Within the fandom, they are commonly referred to by their numbers&#8212;William Hartnell's Doctor is One, Patrick Troughton's is Two, and so on. The current Doctor, played by Peter Capaldi, is Twelve.</p>

<div class="imgWrapper"><img src="images/doctors.jpg" /></div>

<p class="note"><b>Note:</b> There is some confusion about the numbering system due to events that occured during <a href="/list.php#NW4">Series 4</a> and <a href="/list.php#NW7">Series 7</a>. You may sometimes encounter references to "8.5" or "Ten and a half." Generally, however, the above numbering is used within the fandom.</p>

<div class="imgWrapper"><img src="images/doctors 2.png" /></div>

<p>Despite having a mechanic for continuing the show forever, the show's popularity eventually died down. In the 80's, the show lost its way due to behind the scenes drama. The Sixth Doctor was unpopular, and the BBC was facing budget cuts. The show became darker and more violent, and lot of people at the BBC began to hate it, including the Controller of BBC 1. The show was scheduled against <i>Coronation Street</i>, a very popular soap opera, and ratings dropped even more. In 1989, <i>Doctor Who</i> was cancelled, ending the classic series, or "Classic Who."</p>

<div class="imgWrapper"><img src="images/survival.jpg" /><img src="images/somewhere out there.jpg" /></div>

<p>Even though the show was over, the fans never gave up. In the 90's, <i>Doctor Who</i> continued in the form of novels, comedy sketches, audio dramas, and fan films.</p>

<div class="imgWrapper"><img src="images/VNA novel.jpg" /><img src="images/dimensions in time.jpg" /><img src="images/audio drama.jpg" /></div>

<p>By 1996, fans were pushing for a continuation of the show. An American company was interested, but only wanted to commit to a telemovie. It was hoped that <a href="/episodes.php?episodeID=500&seasonID=58"><i>Doctor Who: the Movie</i></a> could lead to the show being picked up and the production of an American reboot; thus, the movie picked up where the show had left off, which was alienating to new viewers. The movie was made, but the show was not continued. <i>Doctor Who</i> remained on haitus.</p>

<div class="imgWrapper"><img src="images/tv movie.png" /><img src="images/dvd cover.jpg" /><img src="images/tvm.jpg" /></div>

<p>Then, in 2005, <i>Doctor Who</i> returned at last, beginning the new series, or "New Who." It was a clean start, inviting to new viewers, and had a budget much larger than that of Classic Who. Most modern fans of <i>Doctor Who</i> have seen only the new series. If you want to start watching <i>Doctor Who</i>, this is where you should start.</p>

<div class="imgWrapper"><img src="images/reboot.jpg" /><img src="images/logo.png" /><img src="images/season 1.jpg" /></div>







