<a name="k9co"></a>
<h2>Introduction to <em>K-9 and Company</em></h2>

<div class="fullSize"><img src="images/k9 and co.jpg" /></div>

<p><strong>What It's About:</strong> <em>"K9 and Company was a proposed Doctor Who spin-off in the early 1980s. Ultimately, only a single pilot episode was produced. The series was to have starred Elisabeth Sladen reprising her role as former companion Sarah Jane Smith, with John Leeson returning as the voice of K9."</em> Sarah Jane goes to see her aunt, but finds her missing. Along with K-9 and her friend Brendan, Sarah Jane gets caught up in a mysterious cult intent on a human sacrifice.</p>

<p><strong>Why You Should Watch It:</strong> Despite never being developed into a full series, <em>K-9 and Company</em> provides a lot of details about Sarah Jane's life without the Doctor. People and events first referenced in <em>K-9 and Company</em> show up later in <a href="/episodes.php?episodeID=470&serialID=51">The Five Doctors</a> and <a href="/episodes.php?episodeID=300&seasonID=30">School Reunion</a>, and also in <em>The Sarah Jane Adventures</em>. It's a fun watch, and more Sarah Jane is always a plus.</p>

<p><strong>Age Rating: 10+</strong> for general scariness.</p>




<br><br>
<a name="k9"></a>
<h2>Introduction to <em>K-9</em></h2>

<div class="fullSize"><img src="images/k9.jpg" /></div>

<p><strong>What It's About:</strong> <em>"K-9 is set 2050 London and revolves around Professor Gryffen, who is experimenting with a Space-Time Manipulator, and his friends, 14-year-olds Starkey and Jorjie and 15-year-old Darius, who runs errands for the professor. After K-9 gets into some trouble and is saved by the team, K-9 and the humans then form the front line of defense against alien menaces from outer space and other times."</em></p>

<p><strong>Why You Should Watch It:</strong> Because <em>K-9</em> is not a BBC production, it legally connot make any references to <em>Doctor Who</em>. The only real relation between the shows is the character of K-9. It's also a children's show. You may want to watch it just for completions' sake.</p>

<p><strong>Age Rating: 6+</strong> for being a children's show.</p>





<br><br>
<a name="probe"></a>
<h2>Introduction to <em>P.R.O.B.E</em></h2>

<div class="fullSize"><img src="images/probe.jpg" /></div>

<p><strong>What It's About:</strong> <em>"P.R.O.B.E stars Caroline John as her Doctor Who character of Liz Shaw, who now works for the Preternatural Research Bureau.</em>" Liz Shaw works to investigate ghosts, mysterious murders, and sinister rituals.</p>

<p><strong>Why You Should Watch It:</strong> <em>P.R.O.B.E</em> was the first ongoing <em>Doctor Who</em> spin-off. It provides more background to Liz Shaw. However, it was not allowed to make overt references to <em>Doctor Who</em>, and some information in the show contradicts what is stated in <em>Doctor Who</em>. You may want to watch it just for completions' sake.</p>

<p><strong>Age Rating: 14+</strong> for horror and gore.</p>



<b><br>
<h2>What to Watch</h2>

<div class="fullSize"><img src="images/selections.png" /></div>

<p>We recommend watching <em>Torchwood</em>, <em>The Sarah Jane Adventures</em>, and <em>Class</em> because they have direct connections to <em>Doctor Who</em>. They're arguably the three best shows. The other spin-offs are fun to watch and nice for completions' sake, but not necessary for watching <em>Doctor Who</em>.</p>






